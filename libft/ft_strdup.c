#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dest;
	int		len;

	if (!s1)
		return (NULL);
	i = 0;
	len = 0;
	while (s1[len] != '\0')
		len++;
	dest = malloc(sizeof(*s1) * (len + 1));
	if (!dest)
		return (NULL);
	while (i < len)
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
